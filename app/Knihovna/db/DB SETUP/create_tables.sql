CREATE TABLE Languages (
    LanguageID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Name VARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE Publishers (
    PublisherID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Name VARCHAR(255) NOT NULL UNIQUE
);

CREATE TABLE Nationalities (
    NationalityID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Name VARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE Authors (
    AuthorID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FirstName VARCHAR(100) NOT NULL,
    LastName VARCHAR(100) NOT NULL,
    DateOfBirth DATE,
    NationalityID INTEGER,
    FOREIGN KEY (NationalityID) REFERENCES Nationalities(NationalityID)
);

CREATE TABLE Books (
    BookID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Name VARCHAR(255) NOT NULL,
    PublishDate DATE,
    PublisherID INTEGER,
    LanguageID INTEGER,
    Description BLOB SUB_TYPE TEXT,
    HaveRead BOOLEAN DEFAULT FALSE,
    Rating SMALLINT CHECK (Rating >= 1 AND Rating <= 5),
    ISBN VARCHAR(13) UNIQUE CHECK (ISBN IS NULL OR ISBN SIMILAR TO '[0-9]{10,13}'),
    FOREIGN KEY (PublisherID) REFERENCES Publishers(PublisherID),
    FOREIGN KEY (LanguageID) REFERENCES Languages(LanguageID)
);

CREATE TABLE BooksAuthors (
    BookID INTEGER,
    AuthorID INTEGER,
    PRIMARY KEY (BookID, AuthorID),
    FOREIGN KEY (BookID) REFERENCES Books(BookID) ON DELETE CASCADE, 
    FOREIGN KEY (AuthorID) REFERENCES Authors(AuthorID) ON DELETE CASCADE 
);

